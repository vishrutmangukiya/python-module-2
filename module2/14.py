# # What are negative indexes and why are they used?



# # Negative indexing is used in Python to manipulate sequence objects such as lists, arrays, strings, etc. Negative indexing retrieves elements from the end by providing negative numbers as sequence indexes.

# In Python, negative indexes are used to access elements in a sequence (such as a list or a string) from the end of the sequence. The index -1 represents the last element, -2 represents the second-to-last element, and so on.

# Here's an example with a list:

# ```python
# my_list = [1, 2, 3, 4, 5]
# ```

# - `my_list[0]` would access the first element, which is `1`.
# - `my_list[1]` would access the second element, which is `2`.
# - `my_list[-1]` would access the last element, which is also `5`.
# - `my_list[-2]` would access the second-to-last element, which is `4`.

